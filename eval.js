function calc_stats()
{
    // ----- HP ----- //
    var base_hp = parseFloat(document.getElementById("basehp").value);
    var hp_pc   = parseFloat(document.getElementById("hppc").value);
    var hp_flat = parseFloat(document.getElementById("hpflat").value);

    // ----- ATK ----- //
    var chr_base = parseFloat(document.getElementById("chrbase").value);
    var wpn_base = parseFloat(document.getElementById("wpnbase").value);
    var atk_pc   = parseFloat(document.getElementById("atkpc").value);
    var atk_flat = parseFloat(document.getElementById("atkflat").value);

    // ----- DEF ----- //
    var base_def = parseFloat(document.getElementById("basedef").value);
    var def_pc   = parseFloat(document.getElementById("defpc").value);
    var def_flat = parseFloat(document.getElementById("defflat").value);

    // ----- CRIT RATE ----- //
    var base_rate  = parseFloat(document.getElementById("baserate").value);
    var crit_pc = parseFloat(document.getElementById("ratepc").value);

    // ----- CRIT DMG ----- //
    var base_cdmg  = parseFloat(document.getElementById("basecdmg").value);
    var cdmg_pc = parseFloat(document.getElementById("cdmgpc").value);
    
    // ----- EM ----- //
    var base_em  = parseFloat(document.getElementById("baseem").value);
    var em_extra = parseFloat(document.getElementById("empc").value);

    // ----- ER ----- //
    var base_er  = parseFloat(document.getElementById("baseer").value);
    var er_extra = parseFloat(document.getElementById("erpc").value);

    // ----- CONVERT PERCENTAGE TO DECIMAL ----- //
    hp_pc = hp_pc / 100;
    atk_pc = atk_pc / 100;
    def_pc = def_pc / 100;

    // ----- STAT FORMULAS ----- //
    var hp = base_hp * (1.0 + hp_pc) + hp_flat;
    var atk = (chr_base + wpn_base) * (1.0 + atk_pc) + atk_flat;
    var def = base_def * (1.0 + def_pc) + def_flat;
    var crate = base_rate + crit_pc;
    var cdmg = base_cdmg + cdmg_pc;
    var em = base_em + em_extra;
    var er = base_er + er_extra;


    // ----- CHANGE NUMBERS ON SCREEN----- //
    document.getElementById("hp_result").innerHTML = Math.round(hp);
    document.getElementById("atk_result").innerHTML = Math.round(atk);
    document.getElementById("def_result").innerHTML = Math.round(def);
    document.getElementById("rate_result").innerHTML = crate + "%"; 
    document.getElementById("cdmg_result").innerHTML = cdmg + "%"; 
    document.getElementById("em_result").innerHTML = em;
    document.getElementById("er_result").innerHTML = er + "%";
}

function change_base()
{
    var x = document.getElementById("chrsel").value;
    for(let i = 0; i < chr_list.length; i++)
    {
        if(chr_list[i].name === x)
        {
            document.getElementById("basehp").value = chr_list[i].hp;
            document.getElementById("chrbase").value = chr_list[i].atk;
            document.getElementById("basedef").value = chr_list[i].def;
            document.getElementById("baserate").value = chr_list[i].rate;
            document.getElementById("basecdmg").value = chr_list[i].cdmg;
            document.getElementById("baseem").value = chr_list[i].em;
            document.getElementById("baseer").value = chr_list[i].er;
            document.getElementById("chrimg").style.backgroundImage = "linear-gradient(rgba(31, 31, 31,0.5), rgba(31, 31, 31,0.8))," + chr_list[i].img;
        }
    }
}


